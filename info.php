                <h2>Tentang Ramadhan: Apa, Kapan, dan Mengapa?</h2>
                <img src="https://vet.upm.edu.my/summer-uploads/20190530085445blobid0.png" width="100%" height="400">
                <p class="mt-2">Bulan Ramadhan menjadi salah satu bulan yang paling ditunggu-tunggu umat Islam di
                    seluruh dunia. Di
                    bulan Ramadhan, Muslim di seluruh dunia tidak makan dan minum sepanjang hari. Di samping itu, mereka
                    juga menghindari hal-hal yang bisa membatalkan puasa seperti keluar air mani karena bersentuhan
                    kulit, masuknya benda ke dalam lubang tubuh, dan lainnya.</p>
                <p>Lantas, apa itu Ramadhan? Berapa lama puasa Ramadhan? Mengapa harus berpuasa? Kapan puasa Ramadhan
                    itu disyariatkan? Apa saja yang membatalkan puasa? Dan apa saja keutamaan bulan Ramadhan? </p>
                <b>Apa itu Ramadhan?</b>
                <p>Ramadhan adalah bulan ke-9 dalam kalender Hijriyah, kalender umat Islam yang menggunakan peredaran
                    bulan sebagai acuannya. Muslim atau Muslimah dewasa yang sehat dan tidak sedang dalam halangan
                    syar’i diwajibkan menjalankan ibadah puasa dalam bulan Ramadhan dari Subuh hingga terbenamnya
                    matahari. Selama bulan Ramadhan, umat Muslim bangun pagi-pagi –sebelum tiba waktu shalat Shubuh-
                    untuk makan sahur. Kemudian mereka berpuasa sepanjang hari. Ketika matahari sudah terbenam, mereka
                    berbuka puasa. Disunnah dengan makanan yang manis. Tidak hanya puasa, umat Muslim juga melaksanakan
                    ibadah lainnya selama bulan Ramadhan seperti membaca Al-Qur’an, shalat tarawih, ngaji, dan lainnya.
                    Bagi umat Muslim, Ramadhan adalah bulan suci. Mereka berlomba-lomba melakukan hal-hal baik dan
                    bermanfaat selama bulan Ramadhan. Umat Muslim percaya kalau Al-Qur’an diturunkan pada bulan suci
                    Ramadhan.</p>
                <b>Berapa lama puasa Ramadhan</b>
                <p>Hari-hari dalam kalender Hijriyah biasanya 29 atau 30 hari. Tergantung kapan hilal (bulan sabit yang
                    menandai awal bulan Hijriyah) terlihat atau tidak. Jika bulan tidak terlihat pada malam hari ke-29,
                    maka Ramadhan akan berlangsung selama 30 hari penuh. Sementara jika hilal terlihat, maka puasa
                    Ramadhan hanya 29 hari.</p>
                <p>Sebetulnya ada dua cara atau metode yang lazim dipakai umat Islam untuk menentukan awal dan akhir
                    bulan Ramadhan, yaitu rukyatul hilal (aktivitas mengamati visibilitas hilal) dan hisab (perhitungan
                    secara matematis dan astronomis untuk menentukan posisi bulan).</p>
                <p>Pemerintah Indonesia melalui Kementerian Agama menggunakan kedua metode tersebut untuk menentukan
                    awal dan akhir Ramadhan. Menurut Menteri Agama Lukman Hakim Saifuddin, kedua metode tersebut saling
                    melengkapi satu sama lain. Bukan untuk diperhadap-hadapkan.</p>
                <p>Umat Islam di Indonesia dan beberapa negara lainnya seperti Arab Saudi, Australia, Singapura,
                    Malaysia, Inggris, dan lainnya mulai menjalankan ibadah puasa Ramadhan 1440 H pada Senin, 6 Mei
                    2019.</p>
                <b>Mengapa harus berpuasa?</b>
                <p>Puasa Ramadhan merupakan salah satu rukun Islam yang lima. Umat Muslim yang dewasa, sehat, dan tidak
                    ada halangan syar’i diwajibkan untuk berpuasa Ramadhan. Kewajiban itu telah ditegaskan Allah dalam
                    QS. Surat al-Baqarah ayat 183: Hai orang-orang yang beriman diwajibkan bagimu ibadah puasa,
                    sebagaimana diwajibkan bagi orang-orang sebelum kalian, agar kalian menjadi orang-orang yang
                    bertaqwa…</p>
                <p>Ibadah puasa juga dipandang sebagai cara untuk belajar tentang kesabaran, menghentikan kebiasaan
                    buruk, dan solidaritas terhadap mereka yang tidak mampu.</p>
                <b>Kapan puasa Ramadhan itu diwajibkan</b>
                <p>Merujuk buku Membaca Sirah Nabi Muhammad saw. dalam Sorotan Al-Qur’an dan Hadis-hadis Shahih (M
                    Quraish Shihab, 2018), berpuasa sebulan penuh selama bulan Ramadhan disyariatkan atau diwajibkan
                    Allah sejak abad ke-2 Hijriyah. Hal itu seiring dengan turunnya wahyu QS. al-Baqarah ayat 183,
                    seperti disebutkan di atas.</p>
                <p>Selain itu, keluar air mani karena bersentuhan kulit, mengalami haid atau nifas, gila, dan murtad
                    juga bisa membatalkan puasa seseorang.</p>
                <b>Apa saja keutamaan bulan Ramadhan</b>
                <p>Bulan Ramadhan memiliki banyak keistimewaan. Diantaranya adalah dosa-dosa di masa lalu diampuni,
                    pintu surga dibuka pintu neraka ditutup, dan setan dibelenggu, waktu dikabulkannya doa, bulan
                    turunnya malam seribu bulan (lailatul qadr), dan penghuni neraka dibebaskan.</p>
                <p>Di samping itu, keutamaan bulan Ramadhan juga adalah doa orang berpuasa dikabulkan, mendapatkan
                    pahala yang sama ketika memberi buka orang yang puasa, mendapatkan pahala seperti haji bagi mereka
                    yang umrah pada bulan Ramadhan, dan lain sebagainya</p>
